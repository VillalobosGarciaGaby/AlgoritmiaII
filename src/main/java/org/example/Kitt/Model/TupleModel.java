package org.example.Kitt.Model;

/**
 * Clase que representa una tupla de dos palabras.
 */
public class TupleModel {

    /**
     * Primera palabra de la tupla.
     */
    public String wordA;

    /**
     * Segunda palabra de la tupla.
     */
    public String wordB;

    /**
     * Constructor de la clase TupleModel.
     *
     * @param wordA Primera palabra de la tupla.
     * @param wordB Segunda palabra de la tupla.
     */
    public TupleModel(String wordA, String wordB) {
        this.wordA = wordA;
        this.wordB = wordB;
    }
}
